# 주어진 전체학기 성적에서 2학기 성적을 출력하세요
# 전체 성적중 85점이 넘는 학기의 점수만 프린트
score = {'1학기':100, '2학기':50, '3학기':88, '4학기':99}
            # 사용을 하려는 딕셔너리 데이터를 생성
print('2학기 성적은', score['2학기'], '점 입니다.')
            # score['2학기']로 key 값인 2학기에 해당하는 value 를 추출
over = []           # 비어있는 over 리스트를 생성
for x in score:     # 맨 처음 만들어 놓은 score 딕셔너리를 범위로 지정해서 for문 반복
    if score[x] >= 85:  # 각 key 값에 해당하는 value가 85 이상인지를 판별
        over.append(score[x])   # 85점 이상인 경우 만들어 놓은 over 리스트에 추가
print('85점을 넘는 학기의 점수는', over)  # 추가한 85점 이상의 학기 점수를 출력
